    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        // TODO add your handling code here:

        char[] mkcu = txtMKCu.getPassword();
        char[] mkmoi = txtMKMoi.getPassword();
        char[] Nhaplaimkmoi = txtNhapLaiMK.getPassword();
        if (String.valueOf(mkcu).equals(ps)) 
        {
            if (String.valueOf(Nhaplaimkmoi).equals(String.valueOf(mkmoi))&&(!String.valueOf(txtMKMoi.getPassword()).equals("") && !String.valueOf(txtNhapLaiMK.getPassword()).equals(""))) 
                {
                    doiMK();
                    dispose();
                } 
            else 
                {
                    JOptionPane.showMessageDialog(null, "Mật khẩu mới không trùng khớp");
                }
        }
        else if (String.valueOf(txtMKMoi.getPassword()).equals("") || String.valueOf(txtNhapLaiMK.getPassword()).equals("")) 
        {
            txtMKCu.requestFocus();
            JOptionPane.showMessageDialog(rootPane, "Bạn vui lòng nhập đầy đủ thông tin", "Lỗi", JOptionPane.ERROR_MESSAGE);
        } 
        else
        {
            JOptionPane.showMessageDialog(null, "Mật khẩu cũ không đúng");
        }
    }//GEN-LAST:event_btnOKActionPerformed

