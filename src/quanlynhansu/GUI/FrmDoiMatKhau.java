    public void doiMK() {
        TaiKhoan ad = new TaiKhoan();
        char[] Nhaplaimkmoi = txtNhapLaiMK.getPassword();
        ad.setMatKhau(String.valueOf(Nhaplaimkmoi));
        ad.setTenDangNhap(us);
        DoiMatKhauDAO addao = new DoiMatKhauDAO();
        if (addao.doiMK(ad) > 0) {
            JOptionPane.showMessageDialog(null, "Đổi mật khẩu thành công");
        } else {
            JOptionPane.showMessageDialog(null, "Đổi mật khẩu thất bại");
        }
    }

