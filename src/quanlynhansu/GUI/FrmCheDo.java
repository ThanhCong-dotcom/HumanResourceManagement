    private boolean checkformBH() {
        if (txtLoaiBH.getText().equals("")) {
            txtLoaiBH.requestFocus();
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa nhập mã phòng ban", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        } else if (!(txtSotheBH.getText().matches(".*[a-zA-Z0-9].*"))) {
            txtSotheBH.requestFocus();
            JOptionPane.showMessageDialog(rootPane, "Số thẻ bảo hiểm không có kí tự đặc biệt", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        } else if (txtNoiCapBH.getText().equals("")) {
            txtNoiCapBH.requestFocus();
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa nhập nơi cấp", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }

    private void loadBH() {
        listbh = bhdao.load();
        modelBH = (DefaultTableModel) tbBaohiem.getModel();
        modelBH.setRowCount(0);
        for (BaoHiem nv : listbh) {
            modelBH.addRow(new Object[]{
                nv.getMaNhanVien(),
                nv.getLoaiBaoHiem(),
                nv.getSoThe(),
                nv.getNgayCap(),
                nv.getNgayHetHan(),
                nv.getNoiCap(),
                nv.getGhiChu(),});
        }
    }

    //click vào table hiển thị dữ liệu ra jtextfiled,combobox,jdate Bảng Bảo Hiểm
    private void displayBH() {
        try {
            int current = tbBaohiem.getSelectedRow();
            bh = listbh.get(current);
            cbxMaNVBaoHiem.setSelectedItem(tbBaohiem.getModel().getValueAt(current, 0));
            txtLoaiBH.setText(tbBaohiem.getModel().getValueAt(current, 1).toString());
            txtSotheBH.setText((String) tbBaohiem.getModel().getValueAt(current, 2));
            txtNoiCapBH.setText((String) tbBaohiem.getModel().getValueAt(current, 5));
            txtGhiChuBH.setText((String) tbBaohiem.getModel().getValueAt(current, 6));
            modelBH = (DefaultTableModel) tbBaohiem.getModel();
            date = new SimpleDateFormat("yyyy-MM-dd").parse((String) modelBH.getValueAt(current, 3));
            date1 = new SimpleDateFormat("yyyy-MM-dd").parse((String) modelBH.getValueAt(current, 4));
            dateNgayCapBH.setDate(date);
            dateNgayHetHanBH.setDate(date1);
        } catch (ParseException ex) {
            Logger.getLogger(FrmCheDo.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    private void insertBH() {
        try {
            bh.setMaNhanVien(cbxMaNVBaoHiem.getSelectedItem().toString());
            bh.setLoaiBaoHiem(txtLoaiBH.getText());
            bh.setSoThe(txtSotheBH.getText());
            try {
                date = dateNgayCapBH.getDate();
                date1 = dateNgayCapBH.getDate();

                String df = new SimpleDateFormat("yyyy-MM-dd").format(date);
                String df1 = new SimpleDateFormat("yyyy-MM-dd").format(date1);
                bh.setNgayCap(df);
                bh.setNgayHetHan(df1);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, e.getMessage());
            }
            bh.setNoiCap(txtNoiCapBH.getText());
            bh.setGhiChu(txtGhiChuBH.getText());
            BaoHiemDAO bhDao = new BaoHiemDAO();
            if (bhDao.insert(bh) > 0) {
                JOptionPane.showMessageDialog(null, "Thêm bảo hiểm thành công");
            } else {
                JOptionPane.showMessageDialog(null, "Mã nhân viên [ " + cbxMaNVBaoHiem.getSelectedItem().toString() + " ] đã tồn tại không thể thêm");
            }
        } catch (HeadlessException e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }

