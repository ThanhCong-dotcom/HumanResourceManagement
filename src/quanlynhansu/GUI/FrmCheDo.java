    private void deleteTP() {
        int index = tbThuongPhat.getSelectedRow();
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn 1 phụ cấp trong bảng để xóa", "Thông Báo", 1);
            return;
        }
        int tk = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa không");
        if (tk == JOptionPane.YES_OPTION) {
            if (tpdao.delete(cbxMaNvThuongPhat.getSelectedItem().toString())) {
                JOptionPane.showMessageDialog(this, "Xóa thưởng phạt thành công", "Thông Báo", 1);
            } else {
                JOptionPane.showMessageDialog(this, "Lỗi hệ thống", "Thông Báo", 0);
            }
        }
    }

