    private void deleteBH() {
        int index = tbBaohiem.getSelectedRow();
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn 1 bảo hiểm trong bảng để xóa", "Thông Báo", 1);
            return;
        }
        int tk = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa không");
        if (tk == JOptionPane.YES_OPTION) {
            if (bhdao.delete(cbxMaNVBaoHiem.getSelectedItem().toString())) {
                JOptionPane.showMessageDialog(this, "Xóa Bảo Hiểm thành công", "Thông Báo", 1);
            } else {
                JOptionPane.showMessageDialog(this, "Lỗi hệ thống", "Thông Báo", 0);
            }

        }
    }
