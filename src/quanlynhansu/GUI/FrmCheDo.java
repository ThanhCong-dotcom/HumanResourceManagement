    private void insertTP() {
        try {
            tp.setMaNhanVien(cbxMaNvThuongPhat.getSelectedItem().toString());
            tp.setLoaiTP(txtLoaiThuongPhat.getText());
            tp.setTien(Integer.parseInt(txtTienThuongPhat.getText()));
            try {
                date = dateNgayThuongPhat.getDate();
                String df = new SimpleDateFormat("yyyy-MM-dd").format(date);
                tp.setNgay(df);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, e.getMessage());
            }
            tp.setGhichu(txtGhiChuThuongPhat.getText());
            if (tpdao.insert(tp) > 0) {
                JOptionPane.showMessageDialog(null, "Thêm thưởng phạt thành công");
            } else {
                JOptionPane.showMessageDialog(null, "Mã nhân viên [ " + cbxMaNvThuongPhat.getSelectedItem().toString() + " ] đã tồn tại không thể thêm");
            }
        } catch (HeadlessException | NumberFormatException e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }

