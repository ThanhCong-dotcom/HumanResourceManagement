    //Thêm nhân viên
    public void insert() {

        try {
            nv.setMaNhanVien(txtManv.getText());
            nv.setMaPB(cbxPhongBan.getSelectedItem().toString());
            nv.setMaHD(cbxHopDong.getSelectedItem().toString());
            nv.setHeSoLuong(Integer.parseInt(txtHSLuog.getText()));
            nv.setTenNV(txtHoten.getText());
            nv.setGioiTinh(cbxGioiTinh.getSelectedItem().toString());
            try {
                date = jXDatePicker1.getDate();
                String df = new SimpleDateFormat("yyyy-MM-dd").format(date);
                nv.setNgaySinh(df);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, e.getMessage());
            }
            nv.setSoCM(txtCMND.getText());
            nv.setDienThoai(txtSDT.getText());
            nv.setTrinhDoHV(txtTinhDoHV.getText());
            nv.setDiaChi(txtDiaChi.getText());
            nv.setEmail(txtEmail.getText());
            nv.setTTHonNhan(txtTinhTrangHN.getText());
            NhanVienDAO Daonv = new NhanVienDAO();
            if (Daonv.insert(nv) > 0) {
                JOptionPane.showMessageDialog(null, "Thêm nhân viên thành công");
            } else {
                JOptionPane.showMessageDialog(null, "Mã nhân viên [ " + txtManv.getText() + " ] đã tồn tại không thể thêm");
            }
        } catch (HeadlessException | NumberFormatException e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }

