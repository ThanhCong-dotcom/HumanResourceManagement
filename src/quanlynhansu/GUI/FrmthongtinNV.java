    private void excel() throws IOException {
        FileOutputStream outFile = null;
        JFileChooser chooser = null;
        try {
              model = (DefaultTableModel) tbNhanvien.getModel();
            HSSFWorkbook workbook = new HSSFWorkbook();
            HSSFSheet sh = workbook.createSheet("Employees sheet");
            Row row = sh.createRow(0);
            for (int i = 0; i < model.getColumnCount(); i++) {
                Cell cell = row.createCell(i);
                cell.setCellValue(model.getColumnName(i));
            }
            for (int i = 0; i < model.getRowCount(); i++) {
                row = sh.createRow(i + 1);
                for (int j = 0; j < model.getColumnCount(); j++) {
                    Cell cell = row.createCell(j);
                    cell.setCellValue(model.getValueAt(i, j).toString());
                }
            }
            chooser = new JFileChooser();
            int i = chooser.showSaveDialog(chooser);
            if (i == JFileChooser.APPROVE_OPTION);
            File file = chooser.getSelectedFile();
            file.getParentFile().mkdirs();
            outFile = new FileOutputStream(file + ".xls");
            try {
                workbook.write(outFile);
            } catch (IOException ex) {
                java.util.logging.Logger.getLogger(FrmThongKe.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(rootPane, "Xuất file thành công");

        } catch (FileNotFoundException ex) {
            java.util.logging.Logger.getLogger(FrmThongKe.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        } finally {
            try {
                outFile.close();
            } catch (IOException ex) {
                java.util.logging.Logger.getLogger(FrmThongKe.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
            }
        }

    }

    private boolean checkform() {
        if (txtManv.getText().equals("")) {
            txtManv.requestFocus();
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa nhập mã nhân viên", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        } else if (txtHoten.getText().equals("")) {
            txtManv.requestFocus();
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa nhập họ tên", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        } else if (txtHSLuog.getText().equals("")) {
            txtManv.requestFocus();
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa nhập HS lương", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        } else if (txtCMND.getText().equals("")) {
            txtManv.requestFocus();
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa nhập CMND", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        } else if (jXDatePicker1.getDate() == null) {
            txtManv.requestFocus();
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa chọn ngày tháng", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        } else if (txtSDT.getText().equals("")) {
            txtManv.requestFocus();
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa nhập SDT", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        } else if (txtTinhDoHV.getText().equals("")) {
            txtManv.requestFocus();
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa nhập trình độ HV", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        } else if (!(txtEmail.getText().matches("^[A-Za-z0-9+_.-]+@(.+)$"))) // đoạn này em sử dụng biểu thức chính quy của java để kiểm tra định dạng email nhập vào
        {
            txtManv.requestFocus();
            JOptionPane.showMessageDialog(rootPane, "Bạn  nhập chưa đúng định dạng  email", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        } else if (txtTinhTrangHN.getText().equals("")) {
            txtManv.requestFocus();
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa nhập tình trạng hôn nhân", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        return true;
    }

